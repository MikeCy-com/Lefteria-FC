async def main():
    client = AsyncIOMotorClient(MONGO_URL)
    db = client[DB_NAME]

    # --- Clear existing fixtures and standings ---
    print("Clearing old fixtures and standings...")
    await db.fixtures.delete_many({})
    await db.standings.delete_many({})

    # --- Insert fixtures ---
    print(f"Inserting {len(FIXTURES)} fixtures...")
    fixture_docs = []
    for date_str, home, away, hs, aws, venue in FIXTURES:
        dt = datetime.fromisoformat(date_str)
        fixture_docs.append({
            "id": str(uuid.uuid4()),
            "home_team": home,
            "home_team_logo": "",
            "away_team": away,
            "away_team_logo": "",
            "home_score": hs,
            "away_score": aws,
            "status": "Completed",
            "match_date": dt.isoformat() + "+00:00",
            "venue": venue,
            "competition": "ΠΑΑΟΚ Α' Όμιλος",
            "season": "2025/26",
            "matchday": None,
            "referee": None,
            "attendance": None,
            "scorers": [],
            "events": [],
            "match_stats": None,
            "created_at": datetime.now(timezone.utc).isoformat(),
        })

    if fixture_docs:
        await db.fixtures.insert_many(fixture_docs)
    print(f"  Inserted {len(fixture_docs)} fixtures")

    # --- Insert standings ---
    print(f"Inserting {len(STANDINGS)} standings...")
    standing_docs = []
    for s in STANDINGS:
        standing_docs.append({
            "id": str(uuid.uuid4()),
            "team_name": s["team_name"],
            "team_logo": "",
            "played": s["played"],
            "won": s["won"],
            "drawn": s["drawn"],
            "lost": s["lost"],
            "goals_for": s["goals_for"],
            "goals_against": s["goals_against"],
            "goal_difference": s["goals_for"] - s["goals_against"],
            "points": s["points"],
            "form": "",
            "competition": "ΠΑΑΟΚ Α' Όμιλος",
            "season": "2025/26",
        })

    if standing_docs:
        await db.standings.insert_many(standing_docs)
    print(f"  Inserted {len(standing_docs)} standings")

    # --- Summary ---
    fc = await db.fixtures.count_documents({})
    sc = await db.standings.count_documents({})
    print(f"\nDone! {fc} fixtures, {sc} standings in database.")
    
    # Show LEFTERIA matches
    our_matches = await db.fixtures.find(
        {"$or": [{"home_team": OUR_TEAM}, {"away_team": OUR_TEAM}]},
        {"_id": 0, "home_team": 1, "away_team": 1, "home_score": 1, "away_score": 1, "match_date": 1}
    ).sort("match_date", 1).to_list(50)
    print(f"\nΛΕΥΤΕΡΙΑ 2024 matches ({len(our_matches)}):")
    for m in our_matches:
        print(f"  {m['match_date'][:10]}  {m['home_team']} {m['home_score']}-{m['away_score']} {m['away_team']}")

    client.close()


if __name__ == "__main__":
    asyncio.run(main())
